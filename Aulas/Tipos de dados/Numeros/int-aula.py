# O que é:
  # um número inteiro podendo ser positivo ou negativo, mas sem decimais.

# Ex.:
a = 3
b = -5

# Função type():
  # mostra qual é o tipo de dado dentro de uma variável.

# Sintaxe:
  # type(variável)

#Ex.:
print(type(a))
print(type(b))

# Conversão:
  # você pode converter números decimais em números inteiros.

# Sintaxe:
  # int(variavel)

  # Terminal
    # número - .decimais

# Ex.:
c = 10.5
d = float(c)
print(d)

# Input:
  # você pode utilizar a função "int()" para converter os dados do input de texto para números inteiros.

# Sintaxe:
  # variavel = int(input("pergunta"))

#Ex.:
e = int(input("Digite um número inteiro"))
print(type(e))
