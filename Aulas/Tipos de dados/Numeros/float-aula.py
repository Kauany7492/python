# O que é:
  # um número com deecimais.

# Ex.:
a = 3.5
b = 5.0

# Função type():
  # mostra qual é o tipo de dado dentro de uma variável.

# Sintaxe:
  # type(variável)

#Ex.:
print(type(a))
print(type(b))

# Conversão:
  # você pode converter números inteiros em números decimais.

# Sintaxe:
  # float(variavel)

  # Terminal
    # número.0

# Ex.:
c = 10
d = float(c)
print(d)
